# Faça um programa que leia umnúmero inteiro e mostre na tela o seu sucessor e seu antecessor.

numero = int(input('Informe um número: '))
antecessor = numero - 1
sucessor = numero + 1

print(f'Você informou o número {numero}, seu antecessor é {antecessor} e seu sucessor é {sucessor}')