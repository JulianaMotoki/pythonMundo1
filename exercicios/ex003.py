# Desafio 003
# Crie um programa que leia dois números e mostre a soma entre eles

n1 = float(input('Digite um número '))
n2 = float(input('Digite outro número '))
som = n1 + n2
sub = n1 - n2
div = n1 / n2
mul = n1 * n2

print(f'A soma de {n1} e {n2} é igual a {som}')
print(f'A subtração de {n1} por {n2} é igual a {sub}')
print(f'A divisão de {n1} por {n2} é igual a {div}')
print(f'A multiplicação de {n1} por {n2} é igual a {mul}')
